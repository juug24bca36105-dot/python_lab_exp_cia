# Aim: 
#   To demonstrate various data types and operators in Python.

# Data types
a = 10
b = 5.5
c = "Python"

print("a =", a)
print("b =", b)
print("c =", c)

print("Type of a:", type(a))
print("Type of b:", type(b))
print("Type of c:", type(c))

# Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)

#output:
#   a = 10
#   b = 5.5
#   c = Python
#   Type of a: <class 'int'>
#   Type of b: <class 'float'>
#   Type of c: <class 'str'>
#   Addition: 15.5
#   Subtraction: 4.5
#   Multiplication: 55.0


