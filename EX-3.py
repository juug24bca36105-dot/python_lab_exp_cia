# String operations
s = "Hello"

print("Original String:", s)

print("Uppercase:", s.upper())
print("Lowercase:", s.lower())

print("Length of string:", len(s))

print("Concatenation:", s + " Python")

#output:
    # Original String: Hello
    # Uppercase: HELLO
    # Lowercase: hello
    # Length of string: 5
    # Concatenation: Hello Python

