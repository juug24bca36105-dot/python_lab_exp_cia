# Aim:
#   To write a program to demonstrate tuple related functions in Python.


t = (10, 20, 30, 20)

print("Original Tuple:", t)

print("Length of tuple:", len(t))
print("Count of 20:", t.count(20))
print("Index of 30:", t.index(30))

#output:    
#   Original Tuple: (10, 20, 30, 20)
#   Length of tuple: 4
#   Count of 20: 2
#   Index of 30: 2  

